var num1=parseInt(prompt( "Number 1" ));
var num2=parseInt(prompt( "Number 2" ));
var opr=prompt( "Operator" );
  if(opr=="+"){
    alert (num1 + num2);

} else if(opr=="-"){
    alert (num1 - num2);

} else if(opr=="*"){
    alert (num1 * num2);

} else if(opr=="/"){
    alert (num1 / num2);

} else if(opr=="%"){
    alert (num1 % num2);
}